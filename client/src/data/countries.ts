/**
 * 국가 코드로 국가 정보 검색
 */
export function getCountryByCode(code: string): Country | undefined {
  return UN_COUNTRIES.find((c) => c.code === code);
}

/**
 * 국가명으로 국가 정보 검색
 */
export function getCountryByName(name: string): Country | undefined {
  return UN_COUNTRIES.find((c) => c.country.toLowerCase() === name.toLowerCase());
}

/**
 * 한국명으로 국가 정보 검색
 */
export function getCountryByKoreanName(name: string): Country | undefined {
  return UN_COUNTRIES.find((c) => c.koreanName?.toLowerCase() === name.toLowerCase());
}
